    private void bTdeletaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTdeletaActionPerformed
        int result = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja realizar essa operação?", null, JOptionPane.YES_OPTION);
         
        if(result == JOptionPane.YES_OPTION) {            
            try {
                SocioDAO bd = new SocioDAO();
                Socio socio = new Socio(Integer.parseInt(tFCodSocio.getText()));
                bd.deletarSocio(socio);
                JOptionPane.showMessageDialog(null, "Operação feita com sucesso!");
                recarrega_socio();
            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(null, e); 
            }     
        }
    }//GEN-LAST:event_bTdeletaActionPerformed

    private void tabelaSocioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaSocioMouseClicked
        DefaultTableModel model = (DefaultTableModel)tabelaSocio.getModel();
        int tableRowIndex = tabelaSocio.getSelectedRow();
        
        tFCodSocio.setText(model.getValueAt(tableRowIndex, 0).toString());
        tFNome.setText(model.getValueAt(tableRowIndex, 1).toString());
        tFTelefone.setText(model.getValueAt(tableRowIndex, 2).toString());
        tAEndereco.setText(model.getValueAt(tableRowIndex, 3).toString());
        tFProfissao.setText(model.getValueAt(tableRowIndex, 4).toString());
        tADocBanco.setText(model.getValueAt(tableRowIndex, 5).toString());
    }//GEN-LAST:event_tabelaSocioMouseClicked

