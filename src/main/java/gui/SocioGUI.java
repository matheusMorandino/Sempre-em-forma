    private void bTsalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTsalvaActionPerformed
        if("".equals(tFTelefone.getText()) || "".equals(tFProfissao.getText()) || "".equals(tFNome.getText()) || "".equals(tFCodSocio.getText()) || "".equals(tAEndereco.getText()) || "".equals(tADocBanco.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para enviar informações.");
            return;
        }
        
        try{
            SocioDAO bd = new SocioDAO();
            Socio socio = new Socio(Integer.parseInt(tFCodSocio.getText()), tFNome.getText(), tFTelefone.getText(), tAEndereco.getText(), tFProfissao.getText(), tADocBanco.getText());
            bd.inserirSocio(socio);
            JOptionPane.showMessageDialog(null, "Operação feita com sucesso!");
            recarrega_socio();
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e); 
        }
    }//GEN-LAST:event_bTsalvaActionPerformed

