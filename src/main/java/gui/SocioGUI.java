    private void mostra_socio() {
        try{
            SocioDAO bd = new SocioDAO();
            ArrayList<Socio> lista = bd.listarSocios();
            DefaultTableModel model = (DefaultTableModel)tabelaSocio.getModel();
            Object[] row = new Object[6];
            for(int i = 0; i < lista.size(); i++) {
                row[0] = lista.get(i).getNroSocio();
                row[1] = lista.get(i).getNome();
                row[2] = lista.get(i).getTelefone();
                row[3] = lista.get(i).getEndereco();
                row[4] = lista.get(i).getProfissao();
                row[5] = lista.get(i).getDocBancarios();
                model.addRow(row);
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void recarrega_socio() {
        DefaultTableModel model = (DefaultTableModel)tabelaSocio.getModel();
        model.setRowCount(0);
        mostra_socio();
    }
    
