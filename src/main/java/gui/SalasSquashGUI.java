    private void mostraSalasSquash() {
        try{
            SalaSquashCompoundSalaDAO bd = new SalaSquashCompoundSalaDAO();
            ArrayList<SalaSquashCompoundSala> lista = bd.listarSalaSquash();
            DefaultTableModel model = (DefaultTableModel)tabelaSalasSquash.getModel();
            Object[] row = new Object[4];
            for(int i = 0; i < lista.size(); i++) {
                row[0] = lista.get(i).getNroSala();
                row[1] = lista.get(i).getArea();
                row[2] = lista.get(i).getLocalizacao();
                row[3] = lista.get(i).getEstado();
                model.addRow(row);
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private int[] createSalasRange(ArrayList<Integer> blackList, int salasMaxRange) {
        int[] salasRange = new int[salasMaxRange + 5];
        
        int i = 1;
        int elementsInSalasRange = 0;
        while(true) {
            salasRange[elementsInSalasRange] = i;
            elementsInSalasRange++;
            i++;
            if(elementsInSalasRange == salasRange.length)
                break;
        }
        
        return salasRange;
    }
    
    private void fillSalasComboBox() {          
        try{
            int salasMaxRange = 1;
            SalaSquashCompoundSalaDAO bd = new SalaSquashCompoundSalaDAO();
            
            ArrayList<Integer> blackList = bd.blackList();
            salasMaxRange = bd.salasMaxRange();
            
            int[] salasRange = createSalasRange(blackList, salasMaxRange);
        
            cBSalas.removeAllItems();
            for(int i = 0; i < salasRange.length; i++) {
                cBSalas.addItem(salasRange[i]+"");
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void recarregaSalasSquash() {
        DefaultTableModel model = (DefaultTableModel)tabelaSalasSquash.getModel();
        model.setRowCount(0);
        mostraSalasSquash();
    }
    
