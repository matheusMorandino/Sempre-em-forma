    private void bTInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTInserirActionPerformed
        if(tFArea.getText().equals("") || tFLocalizacao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para inserir uma nova sala.");
            return;
        }
        
        try{
            SalaSquashCompoundSalaDAO bd = new SalaSquashCompoundSalaDAO();            
            SalaSquashCompoundSala sala = new SalaSquashCompoundSala(Integer.parseInt(cBSalas.getSelectedItem().toString()), Float.parseFloat(tFArea.getText()), tFLocalizacao.getText(), cBEstado.getSelectedItem().toString());
            bd.inserirSalaSquash(sala);
            JOptionPane.showMessageDialog(null, "Operação feita com sucesso!");
            recarregaSalasSquash();
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e); 
        }                     
    }//GEN-LAST:event_bTInserirActionPerformed

    private void bTDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTDeletarActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "You tem certeza que deseja deletar a sala " + cBSalas.getSelectedItem() + "?", "WARNING", JOptionPane.YES_NO_OPTION);
        
        if (response == JOptionPane.YES_OPTION) {           
            try {
                SalaSquashCompoundSalaDAO bd = new SalaSquashCompoundSalaDAO();
                SalaSquashCompoundSala sala = new SalaSquashCompoundSala(Integer.parseInt(cBSalas.getSelectedItem().toString()));
                bd.deletarSalaSquash(sala);                
                JOptionPane.showMessageDialog(null, "Operação feita com sucesso!");
                recarregaSalasSquash();
            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(null, e); 
            }
        }        
    }//GEN-LAST:event_bTDeletarActionPerformed

