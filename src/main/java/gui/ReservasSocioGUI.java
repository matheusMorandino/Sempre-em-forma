    public void mostra_reserva(ArrayList<Reserva> lista) {
        DefaultTableModel model = (DefaultTableModel)tabelaReserva.getModel();
        Object[] row = new Object[3];
        for(int i = 0; i < lista.size(); i++) {
            row[0] = lista.get(i).getNroSocio();
            row[1] = lista.get(i).getNroSala();
            row[2] = lista.get(i).getDataReserva();
            model.addRow(row);
        }
    }
    
    protected void atualizaEstadoComboBox() {
        boolean ativaNome = cBBuscaForma.getSelectedItem().equals("Nome");
        if(ativaNome) {
            tFNome.setEnabled(true);
            tFNroSocio.setEnabled(false);
        } 
        else {
            tFNome.setEnabled(false);
            tFNroSocio.setEnabled(true);
        }
    }
    
