    private void bTBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTBuscarActionPerformed
        if(tFNroSocio.getText().equals("") && tFNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo para buscar informações.");
            return;
        }

        try{
            ArrayList<Reserva> reservas;
            ReservasSocioDAO bd = new ReservasSocioDAO();
            
            if(cBBuscaForma.getSelectedItem().equals("Nome")) {
                Socio socio = new Socio(tFNome.getText());
                reservas = bd.listarReservasPeloNomeSocio(socio);
            }
            else {
                Socio socio = new Socio(Integer.parseInt(tFNroSocio.getText()));
                reservas = bd.listarReservasPeloNroSocio(socio);
            }
            
            DefaultTableModel model = (DefaultTableModel)tabelaReserva.getModel();
            model.setRowCount(0);
            
            if(reservas.isEmpty()) {
                JOptionPane.showMessageDialog(null, "O cliente não possui reservas atualmente.");
            }
            else {
                mostra_reserva(reservas);
            }
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_bTBuscarActionPerformed

